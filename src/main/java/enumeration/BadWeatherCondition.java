package enumeration;

import java.io.Serializable;

public enum BadWeatherCondition implements Serializable {

	NONE("None"), SUNNY("Clear"), CLOUDY("Clouds"), RAINY("Rain"), SNOWY("Snow"), FOGGY(
			"Fog"), MISTY("Mist");

	private String condition;

	private BadWeatherCondition(String string) {
		this.condition = string;
	}

	public String getCondition() {
		return condition;
	}

}
